public class OperadoresdeComparecao {
    public static void main(String[] args) {
        int idade = 25;

        System.out.println("idade == 25: " + (idade == 25));
        System.out.println("idade != 30: " + (idade != 30));
        System.out.println("idade > 18: " + (idade > 18));
        System.out.println("idade < 30: " + (idade < 30));
        System.out.println("idade >= 25: " + (idade >= 25));
        System.out.println("idade <= 25: " + (idade <= 25));
    }
}
